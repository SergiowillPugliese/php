<h1>Art. 3</h1> 

<p>Tutti i cittadini hanno pari dignità sociale e sono uguali davanti alla legge, senza distinzioni di sesso, di razza, di lingua, di religione, di opinioni politiche, di condizioni personali e sociali.</p>
<p>E' compito della Repubblica rimuovere gli ostacoli di ordine economico e siciale, che, limitando di fatto la libertà e l'eguaglianza dei cittadini. impediscono il pieno sviluppo della persona umana e l'effettiva partecipazione di tutti i lavoratori all'organizzazione politica, economica e sociale del Paese.</p>

